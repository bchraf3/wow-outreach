		<section class="values">
			<div class="row">
				<h2> Our Values </h2>
			</div>
			<div class="row">
				<div class="col span-1-of-4 box">
					<ion-icon name="people" class="blue"></ion-icon>
					<h3>community</h3>
					<p> We feel that it is essential for every person who wants to see positive change in their communities to become agents of change. Therefore we have Street outreaches weekly. We work in the schools with our youth and currently have volunteers working in 2 of the Schools in our community as life coaches. Robert is also a public speaker and mentor in many of the schools in our community. W. O. W is closely connected to the Lewende Woord church group where we assist with food parcels to families in need. We also have a close relationship with most of the shelters in our region to help those who have no place to go. </p>
				</div>
				<div class="col span-1-of-4 box">
					<ion-icon name="heart-circle" class="red"></ion-icon>
					<h3>courage</h3>
					<p> We have a brilliant team of volunteers who are not shy or afraid to reach out to the community. One of the greatest limitations to success and personal growth is the fear of failure and the fear of peoples opinions. Our goal is to challenge people to dare to dream again, no matter past mistakes or failures. </p>
				</div>
				<div class="col span-1-of-4 box">
					<ion-icon name="bulb-outline" class="gold"></ion-icon>
					<h3>empowerment</h3>
					<p> We firmly believe just feeding people is not enough. The end goal is to help them break out of the cycle of addiction/poverty that they are in. We do this by praying with them, encouraging them, helping them find places to stay and jobs. We have seen lives radically changed this way. </p>
				</div>
				<div class="col span-1-of-4 box">
					<ion-icon name="eye-outline" class="green"></ion-icon>
					<h3>integrity</h3>
					<p> Christianity has very strong moral ethics, things like patience, friendliness, honesty, love and humility are important characteristics that we believe we should portray and impart. We value the people we serve and do not use their weakness or misery as a means to gain popularity. We don't hire photographers or use drones to invade peoples privacy but treat them with as much care and love as we possibly can. </p>
				</div>
