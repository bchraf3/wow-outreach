				<h2> Our History </h2>
			</div>
			<div class="box">
				<p class="long-copy"> Walking on water was started in 2012 by Robert Pollard. It started off as a small community outreach, where Robert would feed people and hand out clothes. In 2013 W. O. W Outreach was registered as an article 21 N.P.O Since then we have grown considerably to the extend that we have almost 40 volunteers serving with us. </p>
			</div>
			<div class="row">
				<div class="col span-1-of-2 box">
					<h2>vision</h2>
					<p> To bring hope to those in poverty, those who feel destitute and forgotten, by contributing to their basic needs and helping them renew their minds and outlook on life. To establish a trust based relationship with people in order to help them break out of destructive behavioral patterns and vices.</p>
				</div>
				<div class="col span-1-of-2 box">
					<h2>mission</h2>
					<p> Reaching out to schools, communities, homeless and destitute. Empowering people within our community to become more proficient. Restoring people's sense of self worth and purpose by helping them identify their strengths.</p>
				</div>
