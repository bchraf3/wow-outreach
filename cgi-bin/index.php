		<section class="projects" id="projects">
			<div class="row">
				<h2> w.o.w projects </h2>
			</div>
			<div class="row">
				<div class="col span-1-of-2 box"> 
					<h3>streets</h3>
					<p>Every friday night we have a team of volunteers going to a location on the streets where we feed up to 100 people. Here we also encourage and motivate them, distributing clothes, bibles, shoes and blankets on regular intervals. We also work closely with many of the shelters in our community, and often get people off the street into a shelter. The volunteers take turns picking up people from the streets to bring them to church on Sunday nights as well, when pastor Robert Pollard is preaching. The purpose for this is to get the homeless person reintegrated into society by changing their surroundings and the company they keep. Ideally we would love to have a minibus with which to transport the people to shelters, church, clinics etc.</p>
				</div>
				<div class="col span-1-of-2 box"> 
					<h3> community </h3>
					<p>We also offer monthly food parcels to families in the community who are struggling due to unemployment etc. Once a quarter we reach out to a local community group ie: Malusi, TRD and distribute food and clothing in these communities.</p>
				</div>
			</div>
			<div class="row">
				<div class="col span-1-of-2 box"> 
					<h3>schools</h3>
					<p>We also do weekly outreaches to schools:</p>
					<ul>
						<li>Currently we have a projects at Tuine High School called "Jesus Revolution" where we meet with the students once a week, worship together and share some spiritual motivation with them. We also try to have a get together once month where the kids can have fun and connect with each other.</li>
						<li>Charity - We are currently feeding 70 children once a week Hermansstad primary school</li>
						<li>Life coaches, youth workers: We have currently got 2 people working in the schools with the youth, one at Saamspan Primary and one at HTS Tuine Technical High School</li>
						<li>Tutoring: We have recently started offering tutoring to students from 8 - 12 once a week, specifically mathematics,at a cost of only R50 per learner which is very affordable.</li>
						<li>We would love to also offer affordable transport to and from school for children in our community, for this we are still in need of a minibus.</li>
					</ul>
				</div>
				<div class="col span-1-of-2 box">
					<h3> international </h3>
					<p>Robert Pollard, founder of W. O. W has also been to nations like Brazil, India, Phillipines, Kenya, Nigeria, Malawi and Zimbabwe, where he has spent time in the communities, speaking at schools, streets and churches, as well as going from house to house. Ultimately we envision also having offices in these nations, particularly Brazil, Phillipines and Malawi, where Robert has established good relationships with npo's and churches.</p>
					<p>Robert's motto is simple. "Be the reason someone goes to bed smiling! If we want to see communities changed we have to get involved. Your passion and care will be the most contagious characteristics you ever posess. Be an agent of change!"</p>
				</div>
			</div>

